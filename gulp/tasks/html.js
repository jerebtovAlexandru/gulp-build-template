// Импорт плагина для сборки HTML из отдельных компонентов (шапка, подвал, блоки) с помощью инклудов
import fileInclude from "gulp-file-include";
// Импорт плагина для автоматической замены тегов <img> на <picture> с подключением картинок в формате WebP (кроме SVG)
import webpHtmlNosvg from "gulp-webp-html-nosvg";
// Импорт плагина для добавления уникального хэша или даты к путям CSS/JS файлов (борьба с кэшированием у пользователей)
import versionNumber from "gulp-version-number";

// Экспорт именованной функции-задачи для обработки и сборки HTML-страниц
export const html = () => {
  // Возврат потока файлов Gulp для контроля выполнения асинхронного таска
  return (
    app.gulp
      // Метод .src() находит и получает доступ ко всем исходным HTML-файлам по указанному пути из конфигурации
      .src(app.path.src.html)
      // Подключение плагина plumber для перехвата синтаксических ошибок (например, незакрытый инклуд) без падения Gulp
      .pipe(
        app.plugins.plumber(
          // Настройка всплывающих системных оповещений при возникновении ошибок во время сборки разметки
          app.plugins.notify.onError({
            title: "HTML", // Заголовок всплывающего окна уведомления
            message: "Error:<%=error.message %>", // Вывод текста ошибки прямо в системное сообщение
          }),
        ),
      )
      // Сборка HTML-страниц: плагин ищет конструкции @@include('путь') и заменяет их на реальное содержимое компонентов
      .pipe(fileInclude())
      // Замена алиаса '@img/' в путях картинок на корректный относительный путь 'img/' для готового HTML-документа
      .pipe(app.plugins.replace(/@img\//g, "img/"))
      // Логическое условие: теги <img> оборачиваются в <picture> с поддержкой WebP только при продакшен-сборке (app.isBuild)
      .pipe(app.plugins.if(app.isBuild, webpHtmlNosvg()))
      // Логическое условие: добавление версионности к подключаемым стилям и скриптам запускается только в режиме build
      .pipe(
        app.plugins.if(
          app.isBuild,
          versionNumber({
            // В качестве версии используется текущая дата и время (%DT% — Date/Time)
            value: "%DT%",
            // Настройки добавления параметра к путям
            append: {
              key: "_v", // Имя GET-параметра в URL (на выходе будет: style.css?_v=20260615...)
              cover: 0, // Режим покрытия: 0 означает добавление параметра в конец строки, не заменяя имя файла
              to: ["css", "js"], // К каким именно типам подключаемых файлов применить версионность
            },
            // Конфигурация вывода служебной информации
            output: {
              file: "gulp/version.json", // Файл, в котором зафиксируется лог сгенерированных версий сборщика
            },
          }),
        ),
      )
      // Метод .dest() указывает Gulp, в какую именно директорию назначения (папку дистрибутива) перенести готовые HTML-файлы
      // Метод .pipe() последовательно передает результаты работы текущего шага в поток выполнения следующего шага
      .pipe(app.gulp.dest(app.path.build.html))
      // Метод stream() отправляет изменения в поток локального сервера BrowserSync для автоматического обновления страницы на экране
      .pipe(app.plugins.browsersync.stream())
  );
};
